    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) historyTable.getModel();
        model.setRowCount(0);

        for (Transaction t : account.getTransactionHistory().getHistory()) {

            if (t.getAccountId().equals(account.getAccountNumber())) {

                model.addRow(new Object[]{
                    t.getTransactionId(),
                    t.getClass().getSimpleName(),
                    t.getAmount(),
                    t.getStatus()
                });
            }
        }
    }

